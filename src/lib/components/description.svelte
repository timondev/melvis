<p>
	Melvis is the successor to a prototype that demonstrated how a metaverse could be created for both
	the visually impaired and the sighted. It's purpose is to provide assistive tools to both identify
	and explain 3D scenes through auditory and haptic means. The aim is to enable visually impaired
	people to use any type of metaverse without the need for assistance from others.
</p>

